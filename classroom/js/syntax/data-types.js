// undefined
let variableUndefined;
console.log(variableUndefined);

// null
const variableNull = null;
console.log(variableNull);

// boolean
console.log(true);
console.log(false);

// number
console.log(-10);
console.log(10);
console.log(0b1010);
console.log(10.1);
console.log(101e-1);

// string
console.log('IFPB');
console.log(`IFPB`);

// array
const array = [1, 2, 3];
console.log(array);
console.log(array[1]);

// Object (JSON)
const json = { a: 1, b: 2 };
console.log(json);
console.log(json['a']);
console.log(json.a);
